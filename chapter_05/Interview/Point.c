int ComparePoint( const void *_elem1, const void *_elem2 )
{
	Point* elem1 = (Point*)_elem1;
	Point* elem2 = (Point*)_elem2;

	if ( elem1->point < elem2->point ) {
		return 1;
	} else if ( elem1->point > elem2->point ) {
		return -1;
	} else {
		return 0;
	}
}
